package Streamliners.Task0._1_Introduction;

// Program to Find ASCII Value of a character

public class P04
{
    public static void main(String[] args)
    {
        char ch = 's';
        int ascii_value = ch;

        int cast_value = (int)ch;

        System.out.println("Ascii value of " + ch + " is " + ascii_value);
        System.out.println("Ascii value of " + ch + " is " + cast_value);
    }
}
